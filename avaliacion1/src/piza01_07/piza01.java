package piza01_07; //es una forma de organizar y agrupar clases relacionadas en una estructura jerárquica. 

public class piza01 { // se utilizan para encapsular datos y comportamientos relacionados en un solo objeto
	public static void main(String[] args) {
		int i;
		i = 1;
		while (i <= 10) {
			System.out.println(i);
			i = i + 1;
		}
	}
}